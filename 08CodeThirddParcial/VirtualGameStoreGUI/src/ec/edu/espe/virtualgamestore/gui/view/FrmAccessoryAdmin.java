    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Accessory accessory = new Accessory();
        DataConstructor dataConstructor = new DataConstructor();
        accessory.setId(formatFielId.getText());
        accessory.setName(formatFielName.getText());
        accessory.setPrice(formatFielPrice.getText());
        accessory.setColor(comboBoxColor.getSelectedItem().toString());
        accessory.setQuantity(spinnerQuantity.getValue().toString());
        dataConstructor.saveAccessories(accessory);
        JOptionPane.showMessageDialog(null, "Producto agregado");
    }//GEN-LAST:event_btnAddActionPerformed

