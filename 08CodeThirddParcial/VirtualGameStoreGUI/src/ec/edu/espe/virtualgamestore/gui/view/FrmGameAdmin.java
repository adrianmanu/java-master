    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Game game = new Game();
        DataConstructor dataConstructor = new DataConstructor();
        game.setId(formatFielId.getText());
        game.setName(formatFielName.getText());
        game.setPrice(formatFielPrice.getText());
        game.setColor(comboBoxColor.getSelectedItem().toString());
        game.setQuantity(spinnerQuantity.getValue().toString());
        game.setPegi(formatFielPegi.getText());
        dataConstructor.saveGames(game);
        JOptionPane.showMessageDialog(null, "Producto agregado");


    }//GEN-LAST:event_btnAddActionPerformed

