    private void formatFielIdKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formatFielIdKeyTyped
        char c = evt.getKeyChar();
        if (c < '0' || c > '9') {
            evt.consume();
        }
    }//GEN-LAST:event_formatFielIdKeyTyped

    private void formatFielNameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formatFielNameKeyTyped
        char c = evt.getKeyChar();
        if ((c < 'a' || c > 'z') && (c < 'A') | c > 'Z' && (c < 'Ñ') && c != KeyEvent.VK_SPACE) {
            evt.consume();
        }
    }//GEN-LAST:event_formatFielNameKeyTyped

